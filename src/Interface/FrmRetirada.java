    public int lancarCaixaRetaguarda(){
        String historico = JOptionPane.showInputDialog(rootPane, "Informe o Histórico de Movimento", this.retirada.getHistorico());
        Movimentocaixa lancar = new Movimentocaixa();
        lancar.setDataMovimento(this.fechaCaixa.getDataFechamento());
        lancar.setDescricao(historico);
        lancar.setEmpresa(this.fechaCaixa.getEmpresa());
        lancar.setPlanoconta(this.retirada.getPlanoconta());
        lancar.setUsuarioLancamento(this.fechaCaixa.getOperador());
        float valor = retirada.getValorCheque().floatValue() + retirada.getValorChequePre().floatValue() + retirada.getValorDinheiro().floatValue();
        lancar.setValorSaida(valor);
        lancar.setValorEntrada(0f);
        MovimentoCaixaFacade movimentoCaixaFacade = new MovimentoCaixaFacade();
        int idCaixa = 0;
        try {
            movimentoCaixaFacade.salvarCaixa(lancar);
            idCaixa = movimentoCaixaFacade.ultimaMovimentoCaixaSalvo();
        } catch (Exception ex) {
            Logger.getLogger(FrmRetirada.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro Salvar Moviemnto Caixa");
        }
        return idCaixa;
    }
    
    public void excluirLancamentoCaixa(int idLancamento){
        MovimentoCaixaFacade movimentoCaixaFacade = new MovimentoCaixaFacade();
        try {
            movimentoCaixaFacade.excluirCaixa(idLancamento);
        } catch (Exception ex) {
            Logger.getLogger(FrmRetirada.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Erro exluir Caixa");
        }
    }
    
    
    public boolean validarRetirada(){
        if (historicojTextField.getText().length() <= 0) {
            JOptionPane.showMessageDialog(null, "Campo histórico ce de preenchimento obrigatório");
            historicojTextField.requestFocus();;
            return false;
        }else {
            if (this.retirada.getPlanoconta()==0){
                JOptionPane.showMessageDialog(null,"Selecione uma conta");
                contajTextField.requestFocus();
                return false;
            }else {
                return true;
            }
        }
    }

