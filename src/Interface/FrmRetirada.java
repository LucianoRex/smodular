    private void confirmarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarjButtonActionPerformed
        if (validarRetirada()) {
            this.retirada.setValorDinheiro(Formatacao.ConvercaoMonetariaFloat(valorDinheirojTextField.getText()));
            this.retirada.setValorCheque(Formatacao.ConvercaoMonetariaFloat(valorChequejTextField.getText()));
            this.retirada.setValorChequePre(Formatacao.ConvercaoMonetariaFloat(valorChequePrejTextField.getText()));
            this.retirada.setHistorico(historicojTextField.getText());
            this.retirada.setEmpresa(config.getEmpresa().getIdempresa());
            int idCaixa = 0;
            boolean resultado = JOptionPane.showConfirmDialog(null, "Lançar Movimento de Caixa JGDynamic?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0;
            if (resultado) {
                idCaixa = lancarCaixaRetaguarda();
            }
            this.retirada.setIdMovimentoCaixa(idCaixa);
            RetiradaFacade retiradaFacade = new RetiradaFacade();
            try {
                retiradaFacade.salvar(retirada);
                gerarJTableRetira();
                limparCampos();
                confirmarjButton.setEnabled(false);
                cancelarjButton.setEnabled(false);
                excluirjButton.setEnabled(false);
                fecharjButton.setEnabled(true);
                adicionarjButton.setEnabled(true);
                valorDinheirojTextField.setEditable(false);
                valorChequePrejTextField.setEditable(false);
                valorChequejTextField.setEditable(false);
            } catch (Exception ex) {
                Logger.getLogger(FrmRetirada.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_confirmarjButtonActionPerformed

