    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        int linha = retiradajTable.getSelectedRow();
        if (linha>=0){
            RetiradaFacade retiradaFacade = new RetiradaFacade();
            this.retirada = listaRetirada.get(linha);
            if (retirada.getIdMovimentoCaixa()>0){
                excluirLancamentoCaixa(retirada.getIdMovimentoCaixa());
            }
            try {
                retiradaFacade.excluir(retirada);
                limparCampos();
                gerarJTableRetira();
            } catch (Exception ex) {
                Logger.getLogger(FrmRetirada.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

