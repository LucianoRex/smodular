    public void gerarListaBandeirasCartao(){
        BandeiraCartaoController bandeiraCartaoController = new BandeiraCartaoController();
        listaBandeira = bandeiraCartaoController.listarBandeirasCartao();
        if (listaBandeira!=null){
            itensBandeira = new DefaultListModel();
            for(int i=0;i<listaBandeira.size();i++){
                itensBandeira.addElement(listaBandeira.get(i));
            }
            bandeirajList.setModel(itensBandeira);
        }
    }
    
    public void gerarListaFormasPagamento(int idBandeiraCartao){
        FormaPagamentoController formaPagamentoController = new FormaPagamentoController();
        listaFormaPagamentoCartao = formaPagamentoController.getFormaPagamentoBandeiraCartao(idBandeiraCartao);
        if (listaFormaPagamentoCartao!=null){
            itensFormas = new DefaultListModel();
            for(int i=0;i<listaFormaPagamentoCartao.size();i++){
                itensFormas.addElement(listaFormaPagamentoCartao.get(i));   
            }
            formaPagamentojList.setModel(itensFormas);
        }
    }
    
    public void gerarModelFormaPagamento(){
        FormaPagamentoController formaPagamentoController = new FormaPagamentoController();
        formaRecebidas = formaPagamentoController.listarFormaPagamentoValor();
        if (formaRecebidas==null){
            formaRecebidas = new ArrayList<Formapagamento>();
        }
        modelForma = new FormaPagamentoTableModel(formaRecebidas);
        DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
        renderer.setHorizontalAlignment(SwingConstants.RIGHT);
        this.setLocationRelativeTo(this);
        formaPagamentojTable.setModel(modelForma);
        formaPagamentojTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        formaPagamentojTable.getColumnModel().getColumn(1).setPreferredWidth(50);
        formaPagamentojTable.getColumnModel().getColumn(2).setCellRenderer(renderer);
        formaPagamentojTable.getColumnModel().getColumn(2).setPreferredWidth(20);
        formaPagamentojTable.repaint();
    }   
    
    public void salvarFormaPagamento(Formapagamento formapagamento){
        FormaPagamentoController formaPagamentoController = new FormaPagamentoController();
        formaPagamentoController.salvarFormapagamento(formapagamento);
    }
