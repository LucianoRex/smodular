    private void valorjTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorjTextFieldKeyPressed
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            Formapagamento fp = new Formapagamento();
            fp = (Formapagamento) formaPagamentojList.getSelectedValue();
            fp.setValor(Formatacao.ConvercaoMonetariaFloat(valorjTextField.getText()));
            fp.setNumeroParcelas(Integer.parseInt(numeroParcelasjTextField.getText()));
            salvarFormaPagamento(fp);
            valorPagar = valorPagar - fp.getValor();
            valorjTextField.setText(Formatacao.foramtarDoubleString(valorPagar));
            bandeirajTextField.setText("");
            numeroParcelasjTextField.setText("1");
            formaPagamentojTextField.setText("");
            itensFormas = new DefaultListModel();
            formaPagamentojList.setModel(itensFormas);
            bandeirajTextField.requestFocus();
            gerarModelFormaPagamento();
        }
    }//GEN-LAST:event_valorjTextFieldKeyPressed

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        float valorCartao =0;
        for(int i=0;i<formaRecebidas.size();i++){
            if (formaRecebidas.get(i).getBandeiraCartao()!=3){
                valorCartao = valorCartao + formaRecebidas.get(i).getValor();
            }
        }
        Formapagamento fp = new Formapagamento();
        FormaPagamentoController formaPagamentoController = new FormaPagamentoController();
        fp = formaPagamentoController.getFormapagamento(10);
        if (fp!=null){
            fp.setValor(valorCartao);
            salvarFormaPagamento(fp);
        }
        telaVenda.gerarFormaPagamentoCartao();
        this.dispose();
    }//GEN-LAST:event_jMenuItem1ActionPerformed

