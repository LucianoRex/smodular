    private void bandeirajTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_bandeirajTextFieldKeyPressed
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER){
            int idBandeira = Integer.parseInt(bandeirajTextField.getText());
            boolean verifica = false;
            for (int i=0;i<listaBandeira.size();i++){
                if (listaBandeira.get(i).getIdbandeirasCartao()==idBandeira){
                    bandeirajList.setSelectedIndex(i);
                    verifica = true;
                }
            }
            if (verifica){
                gerarListaFormasPagamento(idBandeira);
                numeroParcelasjTextField.requestFocus();
                numeroParcelasjTextField.selectAll();
            }else {
                JOptionPane.showMessageDialog(rootPane, "Bandeira não encontrada");
            }
        }
    }//GEN-LAST:event_bandeirajTextFieldKeyPressed

